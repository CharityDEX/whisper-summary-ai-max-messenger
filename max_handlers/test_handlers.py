    is_link = False
    is_document = False
    video_url = None
    file_name = None

    text = event.message.body.text if event.message.body else None
    attachments = event.message.body.attachments if event.message.body else []
    attachments = attachments or []

    # Check attachment types
    audio_att = next((a for a in attachments if isinstance(a, MaxAudioAttachment)), None)
    video_att = next((a for a in attachments if isinstance(a, MaxVideoAttachment)), None)
    file_att = next((a for a in attachments if isinstance(a, MaxFileAttachment)), None)

    if audio_att:
        audio_type = "voice"
    elif text and is_valid_video_url(text):
        video_url = text
        is_link = True
        audio_type = "video_link"
    elif video_att:
        audio_type = "video"
    elif file_att:
        fname = getattr(file_att.payload, 'filename', '') or ''
        ext = fname.rsplit('.', 1)[-1].lower() if '.' in fname else ''
        if ext in ('wav', 'mp3', 'mp4', 'webm', 'm4a', 'avi', 'mov'):
            audio_type = "document"
            is_document = True
            file_name = fname
